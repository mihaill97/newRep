package stepIT.practice1;

public class DataTypes {
    public static void main(String[] args){
        int myNum = 5;
        long myLong = 5L;
        float myFloatNum = 5.99F;
        double myDoubleNum = 5.99;
        boolean myBool = true;
        char myLetter = 'A';
        char anotherLetter = 65;
        char myAnotherLetter = '\u0041';

        System.out.println("myNum is " + myNum);
        System.out.println("myLong is " + myLong);
        System.out.println("myFloatNum is " + myFloatNum);
        System.out.println("myDoubleNum is " + myDoubleNum);
        System.out.println("myBool is " + myBool);
        System.out.println("myLetter is " + myLetter);
        System.out.println("myAnotherLetter is " + myAnotherLetter);



    }

}
